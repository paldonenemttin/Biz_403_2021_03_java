package com.callor.values.var;

public class Varriable_06 {
	
	public static void main(String[] args) {
		
		//정수형 변수 선언과 clear
		int num1 = 0;
		Integer num2 = 0;
		
		// int, Intger로 선언된 변수는 정수값만
		// 저장할 수 있다.
		num1 =30;
		num2 =30;
		
		//최대 -2의 31승~ 2의 31승 -1까지 범위만
		//  저장할 수 있다
		float num3 = 30.0F;
		float num4 = 30.0F;
		
		long numlong1 = 30L;
		long numlong2 = 30L;
		
		double num5 = 30.0;
		double num6 = 30.0;
	}

}
